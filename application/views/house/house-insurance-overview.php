<h2>Home Insurance Buyer's Guide</h2>
  When searching for home insurance, there are a few   things you should keep in mind. 
  Your policy should cover the cost of   your home and a little more in case you’re displaced by some catastrophe and   need to relocate to a new location. Figure the actual cost, which takes into   account your home’s purchase price and any depreciation versus replacement cost,   which is how much it is to replace or fix your home at current levels. <br />
  <br />
  Find out what kind of exclusions your policy contains. That way you know   in which cases it will and will not pay for damages to your property. Always   check exactly what is and isn’t covered by your policy. While it may cover   things such as theft and fire, it most likely doesn’t protect you against damage   from landslides or flood waters.<br />
  <br />
  Lastly, be aware of your deductible and   whether repairs are going to be affordable should the unthinkable   happen.</p>

