<h2>How your disability plan works</h2>
<p> <strong>Benefit amount<br />
  </strong>The amount you receive is usually based on your annual salary,   commissions, or other forms of income from your employer, depending upon your   disability plan.* You can find out if you qualify and how much you will be paid   by reading your plan summary. This explains the details of your plan, including   any limitations or offsets that may apply, based on other income you are   receiving during your disability (such as offsets for certain Social Security   Disability Income payments). Please read your plan carefully. <br />
 
</p>
<p> <strong>How it works</strong><br />
  You need to file a claim to start getting your payments. The claim should   describe:</p>
<ul>
  <li style="list-style-type: circle;"> Your injury or illness</li>
  <li style="list-style-type: circle;"> How long it will take to recover</li>
  <li style="list-style-type: circle;"> The date you first missed work</li>
</ul>
<p> We will also need medical information about your injury or illness from your   doctor. And you will need to send us medical updates while you are out of work.   This helps us determine if you are eligible for payments.<br />
  
</p>
<p> <strong>Start date</strong><br />
  You need to miss a certain number of work days before your disability pay   starts. This is called an elimination period. During this time, you will   normally use your company paid sick time or vacation. Read your plan summary for   more information. Your human resources person can also help you learn more.<br />

</p>
<p> <strong>Returning to work</strong><br />
  A disability case manager and/or a rehabilitation consultant may work with you   to help you get back to work, if possible. Remember, your illness or injury may   affect your ability to perform the duties of your occupation. You may need   special devices or training to help you return to work and meet your career   goals.</p>
