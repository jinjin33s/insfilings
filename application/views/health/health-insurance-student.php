<h2> Why Student Health Insurance May Cost Less</h2>
Student health insurance often is less expensive than traditional health   insurance programs for reasons that include:
<ul>
  <li>Many Student Health   	Centers provide deep discounts if care is provided on campus. For this   	reason, student health insurance plans don’t need to be as comprehensive as   	traditional plans.</li>
  <li>When students   	complete their education, they are no longer eligible for student health   	insurance, which means that most participants are only enrolled for a   	limited, relatively short period of time. Actuaries point out that the   	longer a person stays enrolled in a particular health insurance pool, the   	more health care he or she will require.</li>
  <li>College students are   	younger and typically healthier than the general population, resulting in   	reduced risk for the insurer. Insurance rates typically reflect that reduced   	risk.</li>
</ul>
<p> The advantages of student health insurance make them an affordable, quality   alternative to more expensive, more comprehensive traditional plans.</p>

