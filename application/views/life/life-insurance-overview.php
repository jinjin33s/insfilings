	Life insurance  is a contract between the policy owner and the insurer, where the insurer agrees to pay a designated beneficiary a sum of money upon the occurrence of the insured individual's or individuals' death or other event, such as terminal illness or critical illness. In return, the policy owner agrees to pay a stipulated amount at regular intervals or in lump sums. There may be designs in some countries where bills and death expenses plus catering for after funeral expenses should be included in Policy Premium. In the United States, the predominant form simply specifies a lump sum to be paid on the insured's demise.<br>
    As with most insurance policies, life insurance is a contract between the insurer and the policy owner whereby a benefit is paid to the designated beneficiaries if an insured event occurs which is covered by the policy.<br>
    The value for the policyholder is derived, not from an actual claim event, rather it is the value derived from the 'peace of mind' experienced by the policyholder, due to the negating of adverse financial consequences caused by the death of the Life Assured.<br>
