	<p><span class="subtitle_s">Term Life Insurance</span></p>
	
        <p><span class="subhed_h2">ART (Annaul Renewable Term Life insurance)</span> provides you Lowest insurance for 1 year term based on age, health and coverage, the <U>3 life insurance premium factors.</U><br>
	<span class="subhed_h3">Pros:</span> Lowest cost of insurance and premium amongst all life insurance products<br>
	<span class="subhed_h3">Cons:</span> Increasing cost of insurance and premium annually</p>
	
        <p><span class="subhed_h2">10 yr, 20 yr, 30 yr term</span> provides coverage at a fixed rate of payments for a limited period of time, the relevant term. After that period expires coverage at the previous rate of premiums is no longer guaranteed and the client must either forgo coverage or potentially obtain further coverage with different payments and/or conditions.<br>
	<span class="subhed_h3">Pros:</span> Affordable premium than permanent life insurance products<br>
	<span class="subhed_h3">Cons:</span> Increase of premium at the end of the term contract and proof of insurability may be required to renew.</p>
	
        <p><span class="subhed_h2">ROP(Return of Premium) 10 yr, 20 yr, 30 yr </span> type of term life insurance policy. The concept is that the policy returns the premiums you have paid for coverage over that fixed term period if coverage is never used.<br>
	<span class="subhed_h3">Pros:</span> Relatively lower premium than permanent life insurance products with return of all premiums paid.<br>
	<span class="subhed_h3">Cons:</span> If policy is cancelled at any time, no money is refunded. Increase of premium at the end of the term contract and proof of insurability may be required to renew.</p>
        <br>
	
        <p><span class="subtitle_s">Permanent Insurance</span></p>
	
        <p><span class="subhed_h2">Whole Life </span> Life insurance policy that remains in force for the insured's whole life and requires (in most cases) premiums to be paid every year into the policy.<br>
	<span class="subhed_h3">Pros:</span> Permanent insurance coverage with guarantee that the policy's cash values will increase regardless of the performance of the company or its experience with death claims<br>
	<span class="subhed_h3">Cons:</span> Relatively higher premium amongst all insurance products</p>
	
        <p><span class="subhed_h2">Universal Life Insurance</span> Type of permanent life insurance based on a cash value. That is, the policy is established with the insurer where premium payments above the cost of insurance are credited to the cash value.<br>
	<span class="subhed_h3">Pros:</span> Flexibility on insurance premium as to pay smaller or larger contribution with cash value build up potential<br>
	<span class="subhed_h3">Cons:</span> Inadequate, inconsistent or premature payments could results in policy lapse, leaving you without insurance protection. Interest rates on premium contribution also varies which may cause higher or catch-up premiums and policy lapse.</p>
	
        <p><span class="subhed_h2">Index Universal Life Insurance</span> Type of universal life insurance provides a downside guarantee of 1% or less, but receive potentially higher upside interest crediting, based on the performance of an outside stock index (such as the Standard and Poors 500, a.k.a. S&amp;P 500).<br>
	<span class="subhed_h3">Pros:</span> Premium contribution is protected from downturns in the market.<br>
	<span class="subhed_h3">Cons:</span> Limited maximum interest crediting potential of 15% or more. Potential loss of principle due to combination of varying market performance and cost of insurance.</p>

        <p><span class="subhed_h2">Variable Universal Life Insurance</span> Type of permanent life insurance that builds a cash value. In a VUL, the cash value can be invested in a wide variety of separate accounts, similar to mutual funds, and the choice of which of the available separate accounts to use is entirely up to the contract owner.<br>
	<span class="subhed_h3">Pros:</span> Potential higher rate of return than any other permanent insurance policies based on account investment performance.<br>
	<span class="subhed_h3">Cons:</span> Potential loss of account cash value in separate account and policy lapse based on investment performance in separate account.</p>
        <br>
        
        <p><span class="subtitle_s">Joint Permanent Life Insurance (Multi-Lives Life Insurance)</span></p>
	
        <p><span class="subhed_h2">Joint Universal Life Insurance</span> Universal life insurance that two lives are insured under single policy that death of both insured triggers benefit pay-out. Policy is established with the insurer where premium payments above the cost of insurance are credited to the cash value.<br>
	<span class="subhed_h3">Pros:</span> Flexibility on insurance premium as to pay smaller or larger contribution with cash value build up potential. Lower cost of insurance than single life universal life insurance.<br>
	<span class="subhed_h3">Cons:</span> Inadequate, inconsistent or premature payments could results in policy lapse, leaving you without insurance protection. Interest rates on premium contribution also varies which may cause higher or catch-up premiums and policy lapse. Both insured must be deceased.</p>
	
        <p><span class="subhed_h2">Joint Variable Universal Life Insurance</span> Variable universal life insurance that two lives are insured under single policy that death of both insured triggers benefit pay-out. Type of permanent life insurance that builds a cash value. In a VUL, the cash value can be invested in a wide variety of separate accounts, similar to mutual funds, and the choice of which of the available separate accounts to use is entirely up to the contract owner(s).<br>
	<span class="subhed_h3">Pros:</span> Potential higher rate of return than any other permanent insurance policies based on account investment performance. Lower cost of insurance.<br>
	<span class="subhed_h3">Cons:</span> Potential loss of account cash value in separate account and policy lapse based on investment performance in separate account. Both insured must be deceased.</p><br>
