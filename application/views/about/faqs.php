                If you receive care from a provider outside of the PPO network, you will pay more for the service. For example, a PPO might pay 90 % of the cost for a visit with an in-network doctor but only 70 % of the cost for a visit to a non-networ
                You will have to pay a copayment for each visit/service. These copayments are usually higher than an HMO copayment but not in every case.
                You will  be responsible for paying an annual deductible.

                If or when you join a Preferred Provider Organization, you should find you have more flexibility than with an HMO, but your total out of pocket costs are likely to be somewhat higher.
